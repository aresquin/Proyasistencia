
package Javabeans;


public class Modulos {
    private int idmod;
    private String name;

    public Modulos() {
    }

    public Modulos(int idmod, String name) {
        this.idmod = idmod;
        this.name = name;
    }

    public int getIdmod() {
        return idmod;
    }

    public String getName() {
        return name;
    }

    public void setIdmod(int idmod) {
        this.idmod = idmod;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
}
